class MathsOperations:

  def add(self,a,b):
    c=a+b
    print(c)
  def sub(self,a,b):
    c=a-b
    print(c)
  def mul(self,a,b):
    c=a*b
    print(c)
  def div(self,a,b):
    c=a/b
    print(c)

obj=MathsOperations()
obj.add(10,5)
obj.sub(10,5)
obj.mul(10,5)
obj.div(10,5)
obj.add(100,200)
obj.sub(1000,200)
obj.mul(1000,1000)



